"""
Feature selection utilities for the WHO HIDR project.

This module contains functions for:
- screening available indicators;
- calculating indicator coverage;
- selecting epidemiologically relevant variables;
- extracting selected indicators from WHO datasets.

The objective is to keep feature selection decisions transparent,
reproducible, and separated from the notebook workflow.
"""


import pandas as pd



# =============================================================================
# Indicator screening
# =============================================================================


def calculate_indicator_coverage(
    datasets: dict
) -> pd.DataFrame:
    """
    Calculate geographical and temporal coverage of each indicator.

    Parameters
    ----------
    datasets : dict
        Dictionary containing dataset names and DataFrames.

    Returns
    -------
    pd.DataFrame
        Indicator coverage summary.
    """

    coverage = []


    # Calculate coverage for each dataset
    for dataset_name, df in datasets.items():

        summary = (
            df.groupby("indicator_name")
            .agg(
                countries=("setting", "nunique"),
                years=("date", "nunique"),
                observations=("estimate", "count"),
                missing=(
                    "estimate",
                    lambda x: x.isna().mean() * 100
                ),
            )
            .reset_index()
        )


        summary["dataset"] = dataset_name

        coverage.append(summary)


    coverage = pd.concat(
        coverage,
        ignore_index=True
    )


    # Reorder columns
    coverage = coverage[
        [
            "dataset",
            "indicator_name",
            "countries",
            "years",
            "observations",
            "missing",
        ]
    ]


    return coverage



# =============================================================================
# Display indicator screening results
# =============================================================================


def get_top_indicators(
    coverage: pd.DataFrame,
    dataset_name: str,
    n: int = 10
) -> pd.DataFrame:
    """
    Return indicators with highest geographical and temporal coverage.

    Parameters
    ----------
    coverage : pd.DataFrame
        Coverage dataframe generated by calculate_indicator_coverage.

    dataset_name : str
        Dataset to filter.

    n : int
        Number of indicators returned.

    Returns
    -------
    pd.DataFrame
        Top indicators.
    """


    result = (
        coverage[
            coverage["dataset"] == dataset_name
        ]
        .sort_values(
            [
                "countries",
                "years",
                "observations",
            ],
            ascending=False
        )
        .head(n)
    )


    return result



def display_screening_results(
    coverage: pd.DataFrame,
    n: int = 10
) -> None:
    """
    Display top indicators from each dataset.

    Used during exploratory indicator screening.
    """


    for dataset_name in sorted(
        coverage["dataset"].unique()
    ):

        print("=" * 80)
        print(f"Dataset: {dataset_name}")
        print("=" * 80)


        display(
            get_top_indicators(
                coverage,
                dataset_name,
                n
            )
        )



# =============================================================================
# Feature extraction
# =============================================================================


def extract_selected_indicators(
    datasets: dict,
    selected_features: dict
) -> dict:
    """
    Extract selected indicators from WHO datasets.

    Parameters
    ----------
    datasets : dict
        Original WHO datasets.

    selected_features : dict
        Dictionary containing dataset names and selected indicators.

    Returns
    -------
    dict
        Dictionary containing only selected indicators.
    """


    selected_data = {}


    # Extract only predefined indicators
    for dataset_name, indicators in selected_features.items():

        df = datasets[dataset_name]


        selected_data[dataset_name] = (
            df[
                df["indicator_name"]
                .isin(indicators)
            ]
            .copy()
        )


    return selected_data